<h2>Our History</h2>

<p>
Bible Reading International (BRI) was founded in 1987 in Des Moines, Iowa by a small group
of pastors, teachers, and volunteers who shared a common mission: to make the Bible more
accessible and understandable to people around the world. What began as a local outreach
effort quickly grew into a broader movement focused on encouraging daily Bible reading
and spiritual growth.
</p>

<p>
In its early years, BRI distributed printed Bible reading guides and study materials
to churches and community groups across the Midwest. As interest grew, the organization
expanded its reach nationally, partnering with schools, ministries, and nonprofit
organizations to promote biblical literacy.
</p>

<p>
By the late 1990s, BRI embraced emerging technology by launching its first website,
allowing users to access Bible reading plans and lessons online. This marked a major
turning point, enabling the organization to connect with an international audience
for the first time.
</p>

<p>
Throughout the 2000s and 2010s, Bible Reading International continued to grow,
developing digital resources, lesson programs, and online tools designed to help
individuals study Scripture more effectively. The organization also introduced
multiple Bible versions and structured lessons to support readers of all backgrounds
and experience levels.
</p>

<p>
Today, BRI serves a global audience, providing accessible Bible content, educational
resources, and guidance for those seeking to deepen their understanding of Scripture.
While the methods have evolved, the mission remains the same: to inspire people
everywhere to read, understand, and live by the Word of God.
</p>

<h2>Our Mission</h2>

<p>
Our mission is to encourage consistent Bible reading, promote understanding of
Scripture, and provide resources that help individuals grow in their faith.
</p>

